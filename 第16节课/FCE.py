# 导入所需的NumPy数学库
import numpy as np

# 第1步:定义评价等级
grades = ["优秀", "良好", "中等", "较差", "差"]
# 第2步:构建模糊关系矩阵(R)
# 每一行表示一个指标对每个等级的隶属度
R = np.array([
    [0.7, 0.2, 0.1, 0.0, 0.0],
    # 思想品德
    [0.2, 0.5, 0.3, 0.0, 0.0],
    # 学习成绩
    [0.8, 0.1, 0.1, 0.0, 0.0],
    # 身体素质
    [0.3, 0.5, 0.2, 0.0, 0.0]])
# 综合能力
# 第3步:构建权重向量(W)
# 表示各指标的相对重要性
W = np.array([0.35, 0.30, 0.20, 0.15])
# 第4步:模糊综合运算(W*R)
# 点乘操作,得到最终模糊综合评判向量
B = np.dot(W, R)
# 第5步:输出模糊综合向量B
print("模糊综合评判结果B向量为:", B)
# 第6步:最大隶属度原则进行评价
# 获取最大隶属度所在的索引
max_index = np.argmax(B)
final_result = grades[max_index]
# 第7步:输出最终评价等级
print("最终综合评价结果为：", final_result)
